def калькулятор():
    while True:
        выражение = input("Введите математическое выражение (или 'exit' для выхода): ")
        if выражение == "exit":
            break
        try:
            результат = eval(выражение)
            print("Результат:", результат)
        except Exception as e:
            print("Ошибка:", e)

калькулятор()